class EthereumConnexion {
    Insance = undefined;
    InsanceWithSigner = undefined;

    static CreateInstances(){
        if (EthereumConnexion.Insance === undefined) {
            EthereumConnexion.Insance = new ethers.Contract(contractAddress, abi, provider);

            if (tokenInstanceWithSigner === undefined){
                const signer = provider.getSigner();
                EthereumConnexion.InsanceWithSigner = EthereumConnexion.Insance.connect(signer);
            }
        }
    }

    static GetInstance(){
        return EthereumConnexion.Insance;
    }

    static GetInsanceWithSigner(){
        return EthereumConnexion.InsanceWithSigner;
    }
};

export default EthereumConnexion;

